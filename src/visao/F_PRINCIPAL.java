    private void mnuReativarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnuReativarClienteActionPerformed
        //Primeiro verifica se tem registros inativados, se positivo abre o formulario
        conexao.conectar();
        conexao.ExecutarPesquisaSQL("select * from tblclientes where status='INATIVO'");
        try {
            if (conexao.rs.next())
            {
                tabela = "TBLCLIENTES"; 
                status = "INATIVO";   //variavel determina que seja aberto o formulario somente com INATIVOS                
                F_REATIVARCLIENTES frm = new F_REATIVARCLIENTES();
                frm.setVisible(true); 
            }else{
                JOptionPane.showMessageDialog(null, "No momento não foram encontrados clientes inativados!");   
            }
        } catch (SQLException ex) 
        {
            JOptionPane.showMessageDialog(null, "A consulta não pode ser executada, verifique!\nErro: " + ex.getMessage());            
        }finally{
             conexao.desconectar();
        }
    }//GEN-LAST:event_mnuReativarClienteActionPerformed

