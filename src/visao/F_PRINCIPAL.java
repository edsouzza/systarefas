    private void mnuReativarPatrimonioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnuReativarPatrimonioActionPerformed
        //Primeiro verifica se tem registros inativados, se positivo abre o formulario
        conexao.conectar();
        conexao.ExecutarPesquisaSQL("select * from tblpatrimovel where status='INATIVO'");
        try {
            if (conexao.rs.next())
            {
                tabela = "TBLPATRIMOVEL"; 
                status = "INATIVO";   //variavel determina que seja aberto o formulario somente com INATIVOS                
                F_REATIVARPATRIMOVEL frm = new F_REATIVARPATRIMOVEL();
                frm.setVisible(true); 
            }else{
                JOptionPane.showMessageDialog(null, "No momento não foram encontrados patrimônios inativados!");   
            }
        } catch (SQLException ex) 
        {
            JOptionPane.showMessageDialog(null, "A consulta não pode ser executada, verifique!\nErro: " + ex.getMessage());            
        }finally{
             conexao.desconectar();
        }
    }//GEN-LAST:event_mnuReativarPatrimonioActionPerformed

    private void mnuConsInformacoesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnuConsInformacoesActionPerformed
        //Primeiro verifica se tem registros na tabela de tipos de documentos, se positivo abre o formulario
        conexao.conectar();
        conexao.ExecutarPesquisaSQL("select * from tblusuarios where rf='D631863'");
        try {
            if (conexao.rs.next())
            {
                if(rfUsuario.equals(conexao.rs.getString("rf"))){                
                    tabela = "TBLINFORMACOES";        
                    F_ACESSOS frm = new F_ACESSOS();
                    frm.setVisible(true); 
                }else{   
                    JOptionPane.showMessageDialog(null, "Atenção você não tem permissão para este tipo de acesso!", "Sem permissão de acesso!", 2);
                }
            }
        } catch (SQLException ex) 
        {
            JOptionPane.showMessageDialog(null, "A consulta não pode ser executada, verifique!\nErro: " + ex.getMessage());            
        }finally{
             conexao.desconectar();
        }
   
    }//GEN-LAST:event_mnuConsInformacoesActionPerformed

