package modelo;

public class NomeEstacao {
     private int codigo;
     private String depto;
     private String nomestacao;
     private int numestacao;   
     private String status;

    /**
     * @return the codigo
     */
    public int getCodigo() {
        return codigo;
    }

    /**
     * @param codigo the codigo to set
     */
    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    /**
     * @return the depto
     */
    public String getDepto() {
        return depto;
    }

    /**
     * @param depto the depto to set
     */
    public void setDepto(String depto) {
        this.depto = depto;
    }

    /**
     * @return the numestacao
     */
    public int getNumestacao() {
        return numestacao;
    }

    /**
     * @param numestacao the numestacao to set
     */
    public void setNumestacao(int numestacao) {
        this.numestacao = numestacao;
    }

    /**
     * @return the nomestacao
     */
    public String getNomestacao() {
        return nomestacao;
    }

    /**
     * @param nomestacao the nomestacao to set
     */
    public void setNomestacao(String nomestacao) {
        this.nomestacao = nomestacao;
    }

    /**
     * @return the status
     */
    public String getStatus() {
        return status;
    }

    /**
     * @param status the status to set
     */
    public void setStatus(String status) {
        this.status = status;
    }

 
   
         
}
